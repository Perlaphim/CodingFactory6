package gr.aueb.cf.ch15.immutable;

public class ImmutablePoint {
    private int x;
    private int y;

    public ImmutablePoint() {

    }

    public ImmutablePoint(int x, int y) {
        this.x = x;
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public int getY() {
        return y;
    }

    @Override
    public String toString() {
        return "ImmutablePoint{" +
                "x=" + x +
                ", y=" + y +
                '}';
    }
}
